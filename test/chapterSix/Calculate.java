package chapterSix;

public class Calculate {

    public int canAdd(int firstNumber, int secondNumber) {
        return  firstNumber + secondNumber;
    }

    public int canSubtract(int firstNumber, int secondNumber) {
        return firstNumber - secondNumber;
    }

    public int canMultiply(int num1, int num2) {
        return num1 * num2;
    }

    public int canDivide(int num1, int num2) {
        return num1/num2;
    }

    public int canModulo(int num1, int num2) {
        return num1%num2;
    }
}
